#!/usr/bin/env bash
#
# Generate charts/omnia/templates/clusterrole.yaml from config/rbac/role.yaml.
#
# Single-source-of-truth for operator RBAC:
#   +kubebuilder:rbac markers in Go code
#        ↓ (make manifests)
#   config/rbac/role.yaml                 ← kubebuilder-owned, generated
#        ↓ (this script, via make manifests)
#   charts/omnia/templates/clusterrole.yaml  ← derived, do not hand-edit
#
# Before this script existed, the Helm chart's ClusterRole was hand-edited
# in parallel with the kustomize output. The two drifted silently — #731
# shipped *two* separate RBAC regressions that resulted: a missing
# `clusterrolebindings` verb on the kustomize side, and a missing
# `agent-workspace-reader` ClusterRole on the kustomize side. Both bugs
# lived on main for ~3 weeks. See #733 for the full story.
#
# To add a new permission: add a +kubebuilder:rbac marker above a Reconcile
# method (not a helper function — controller-gen only scans canonical blocks),
# then run `make manifests`. This script regenerates the Helm clusterrole
# template automatically.
#
# Notes on the output format:
#   - The generator writes verbatim rules, so the Helm chart no longer
#     gates arena or workspace rules behind .Values.enterprise.enabled /
#     .Values.workspaces.enabled. This is a small, intentional behavior
#     change: users get permissions for CRDs that may not be installed,
#     which is harmless (a ClusterRole granting access to a missing
#     resource simply has no effect). In exchange, kustomize and Helm
#     deployments are guaranteed to have identical operator permissions.
#   - Non-shared Helm ClusterRoles (workspace-owner/editor/viewer,
#     agent-workspace-reader, metrics-reader) are still hand-maintained.
#     hack/verify-rbac-sync.sh catches drift on those.
#
# Usage:
#   ./hack/sync-helm-rbac.sh        # regenerate
#   ./hack/sync-helm-rbac.sh --check  # fail if the generated file is stale

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

CORE_SOURCE="$REPO_ROOT/config/rbac/role.yaml"
EE_SOURCE="$REPO_ROOT/ee/config/rbac/role.yaml"
TARGET="$REPO_ROOT/charts/omnia/templates/clusterrole.yaml"

MODE=${1:-write}

if ! command -v yq >/dev/null 2>&1; then
    echo "error: yq not found in PATH" >&2
    exit 2
fi

if [ ! -f "$CORE_SOURCE" ]; then
    echo "error: $CORE_SOURCE not found — run 'make manifests' first" >&2
    exit 2
fi

# Extract rules from both core and enterprise controller-gen outputs.
# The Helm chart historically merged enterprise rules (jobs.batch,
# sessionprivacypolicies, etc.) into the single manager-role behind an
# enterprise.enabled conditional. We include them unconditionally — a
# ClusterRole granting access to an uninstalled CRD has no effect.
core_rules=$(yq eval '.rules' "$CORE_SOURCE" | sed 's/^/  /')
ee_rules=""
if [ -f "$EE_SOURCE" ]; then
    ee_rules=$(yq eval '.rules' "$EE_SOURCE" | sed 's/^/  /')
fi
rules_body="$core_rules"
if [ -n "$ee_rules" ]; then
    rules_body="$rules_body
$ee_rules"
fi

GENERATED=$(cat <<EOF
# Generated by hack/sync-helm-rbac.sh — DO NOT EDIT.
#
# Source of truth: +kubebuilder:rbac markers in Go code. Rules flow from Go
# → controller-gen → config/rbac/role.yaml + ee/config/rbac/role.yaml → this
# file. To change operator permissions, edit the marker above the relevant
# Reconcile method and run 'make manifests'. See issue #733.
{{ if .Values.rbac.create -}}
apiVersion: rbac.authorization.k8s.io/v1
kind: ClusterRole
metadata:
  name: {{ include "omnia.fullname" . }}-manager-role
  labels:
    {{- include "omnia.labels" . | nindent 4 }}
rules:
$rules_body
{{- end }}
EOF
)

if [ "$MODE" = "--check" ]; then
    if [ ! -f "$TARGET" ]; then
        echo "error: $TARGET missing — run hack/sync-helm-rbac.sh" >&2
        exit 1
    fi
    if ! diff -u <(echo "$GENERATED") "$TARGET" >/dev/null; then
        echo "error: $TARGET is out of date with $CORE_SOURCE" >&2
        echo "run: hack/sync-helm-rbac.sh" >&2
        echo "" >&2
        diff -u <(echo "$GENERATED") "$TARGET" >&2 || true
        exit 1
    fi
    echo "✓ $TARGET is up to date with $CORE_SOURCE"
    exit 0
fi

echo "$GENERATED" > "$TARGET"
echo "✓ wrote $TARGET from $CORE_SOURCE"
